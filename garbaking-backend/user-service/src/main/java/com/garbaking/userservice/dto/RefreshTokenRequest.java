import lombok.NoArgsConstructor;

/**
 * Refresh Token Request DTO
 *
 * Request body for refreshing access tokens.
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class RefreshTokenRequest {

    @NotBlank(message = "Refresh token is required")
    private String refreshToken;
}
